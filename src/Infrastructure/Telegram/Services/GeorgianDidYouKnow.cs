    private static string Label(int i)
        => $"💡 Интересный факт {i + 1}/{Facts.Length}\n{Facts[i]}";

    /// <summary>A fresh random fact each call — keeps repeated pushes from feeling stale.</summary>
    internal static string Pick()
        => Label(Random.Shared.Next(Facts.Length));

    /// <summary>Deterministic fact-of-day (kept for reproducible callers/tests); wraps safely.</summary>
    internal static string ForDay(int dayOfYear)
        => Label(((dayOfYear % Facts.Length) + Facts.Length) % Facts.Length);
